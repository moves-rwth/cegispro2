const N = 200000;

module grid

	x : [0..N] init 1;
	s : [1..5] init 1;

	[] (x=1) -> (0.5): (x'=x-1) + (1/10): (s'=1)&(x'=x+1) 
				    + (1/10): (s'=2)&(x'=x+2) 
                                    + (1/10): (s'=3)&(x'=x+3)
				    + (1/10): (s'=4)&(x'=x+4)
			            + (1/10): (s'=5)&(x'=x+5);


        [] (x>1)&(x<N) -> (0.5): (x'=x-1) + (0.5):(x'=x+s);

endmodule


label "goal" = x=N;
